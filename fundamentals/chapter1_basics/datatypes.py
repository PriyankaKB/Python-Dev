# datatypes.py
# Examples and explanations for Python data types

# Integer
num = 42
print('Integer:', num)

# Float
pi = 3.14159
print('Float:', pi)

# String
name = "Alice"
print('String:', name)

# Boolean
is_valid = True
print('Boolean:', is_valid)

# NoneType
nothing = None
print('NoneType:', nothing)
