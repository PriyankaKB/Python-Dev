# Example code for strings

# String concatenation
first = "Hello"
second = "World"
message = first + ", " + second + "!"
print(message)

# String formatting
name = "Alice"
greeting = f"Hi, {name}!"
print(greeting)

# String methods
text = "python programming"
print(text.title())
print(text.upper())
print(text.replace("python", "awesome"))
