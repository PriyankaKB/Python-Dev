# examples.py
# Example code for collections (lists, tuples, sets, dicts)

# List
numbers = [1, 2, 3]
numbers.append(4)
print('Numbers:', numbers)

# Tuple
colors = ('red', 'green', 'blue')
print('Colors:', colors)

# Set
letters = {'a', 'b', 'c'}
letters.add('d')
print('Letters:', letters)

# Dictionary
student = {'id': 1, 'name': 'John'}
print('Student:', student)
